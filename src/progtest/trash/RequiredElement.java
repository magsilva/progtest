package progtest.trash;

public class RequiredElement {
	
	private String method;
	
	private String allNodes;
	
	private String allEdges;
	
	private String allUses;
	
	private String allPotUses;

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getAllNodes() {
		return allNodes;
	}

	public void setAllNodes(String allNodes) {
		this.allNodes = allNodes;
	}

	public String getAllEdges() {
		return allEdges;
	}

	public void setAllEdges(String allEdges) {
		this.allEdges = allEdges;
	}

	public String getAllUses() {
		return allUses;
	}

	public void setAllUses(String allUses) {
		this.allUses = allUses;
	}

	public String getAllPotUses() {
		return allPotUses;
	}

	public void setAllPotUses(String allPotUses) {
		this.allPotUses = allPotUses;
	}

}
